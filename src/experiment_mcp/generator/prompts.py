"""Prompt template + the JSON Schema for :class:`ToolSpec`.

Pure module — no I/O, no LLM calls. Adapter tests can assert prompt
shape without mocking subprocess or the SDK.

Symmetric with ``psyneulink_mcp.generator.prompts``. Two divergences are
intentional:

* The prose names the wrapped libraries (``SweetPea`` / ``SweetBean``)
  rather than ``PsyNeuLink``. This is text the LLM reads — it should
  reflect the actual domain.
* The method addendum is generic. Where the sibling has hand-tuned
  per-method addenda for ``Composition.add_node`` / ``add_projection``
  / ``add_linear_processing_pathway`` (each accumulated from real
  agent-failure history), experiment-mcp has none yet. Per-symbol
  hints get added here as the regen + feedback loop surfaces them.
"""

from __future__ import annotations

from typing import Any

from .introspection import SymbolMeta

TOOL_SPEC_SCHEMA: dict[str, Any] = {
    "type": "object",
    "properties": {
        "description": {
            "type": "string",
            "description": (
                "WHEN-to-call focused tool description for an LLM consumer. "
                "1-3 sentences. Emphasize when to call, not just what."
            ),
        },
        "parameters": {
            "type": "object",
            "description": (
                "JSON Schema for the tool's arguments. Standard "
                '{"type": "object", "properties": {...}, "required": [...]} shape.'
            ),
        },
        "notes": {
            "type": "string",
            "description": (
                "Free-form caveats / known gotchas for the agent. "
                "May be the empty string if there is nothing to flag."
            ),
        },
    },
    "required": ["description", "parameters", "notes"],
    "additionalProperties": False,
}


PROMPT_TEMPLATE = """\
You are generating an MCP tool specification for a {root_alias} symbol.

The host system wraps your output into a Python module that registers a single
MCP tool. You only emit the metadata — the implementation body is generated by
a fixed template that calls `{root_module}.{short_name}` with the agent's kwargs.

This MCP wraps SweetPea (factorial design) and SweetBean (jsPsych DSL). Tools
in this server compose: SweetPea generates trial sequences for an experimental
design; SweetBean turns trial sequences into runnable jsPsych experiments. A
typical agent workflow is to (1) declare factors and crossings in SweetPea,
(2) sample a sequence, (3) wire the sampled levels into SweetBean stimuli /
trials / blocks, (4) compile to HTML or run headless. Reflect that workflow
when you write the description — say WHERE in the pipeline this tool fits.

Symbol:  {qualname}
Kind:    {kind}
Module:  {module}
{kind_addendum}
--- DOCSTRING ---
{docstring}

--- SOURCE ---
{source}

--- RECENT FEEDBACK ---
{feedback}

Return a ToolSpec with these fields:

- description: 1-3 sentences. WHEN should an agent call this tool? What does
  the result look like? Optimize for an LLM consumer reading the description
  while deciding which tool to call. Do NOT just restate the docstring.

- parameters: a JSON Schema describing the keyword arguments the agent should
  pass. The host instantiates `{qualname}` with these arguments (for classes)
  or calls it (for functions). Choose argument names, types, defaults, and
  required-set from the symbol's signature + docstring. Use narrow JSON Schema
  types (string, integer, number, boolean, array, object) — avoid `any`.
  Include short per-property `description` strings. The schema's outer shape
  must be an object: {{"type": "object", "properties": {{...}}, "required": [...]}}.

  When a parameter expects a sweetpea/sweetbean object that another tool
  produced (Block, Factor, Trial, Experiment, …), describe it as a HANDLE
  STRING (the same string the producer-tool returned). The runtime resolves
  handle strings to live objects before dispatch.

- notes: caveats the agent should know — silent defaults, units, dimensional
  expectations, surprising behavior, deprecations, anything in the source that
  would trip an LLM. Empty string if there is nothing to flag.

If RECENT FEEDBACK is non-empty, address it directly in the description,
parameters, or notes — that's what the feedback loop is for.

Output ONLY the structured ToolSpec; no prose, no markdown.
"""


# Generic method-kind addendum. Unlike the sibling's per-method overrides,
# experiment-mcp has none yet — this template runs for every method-kind
# symbol. Hand-tuned addenda land here once the feedback loop produces
# concrete agent-failure history for a given method.
_METHOD_ADDENDUM_TEMPLATE = """
Method-kind contract (read carefully — this changes what you should put in
description / parameters / notes):

- The generated tool is bound to a live `{class_short_name}` instance at call
  time. The agent passes that instance as a HANDLE STRING under a kwarg whose
  natural name is the lowercased class name (e.g. an `experiment` handle for
  `Experiment.<method>`, a `block` handle for `Block.<method>`). Your
  `parameters` schema MUST list that kwarg as a string property and put it in
  `required`. Describe it as the handle returned by the constructor tool for
  that class.

- Do NOT include the `self` parameter of the underlying Python method in your
  schema — it's managed by the runtime.

- Other parameters that themselves take handle types should be documented in
  handle-string terms. The runtime helper resolves every handle kwarg from
  string → live object before dispatching the call.
"""


def render_prompt(symbol: SymbolMeta, feedback: list[dict] | None = None) -> str:
    """Render the user-prompt for one symbol.

    ``feedback`` is the merged list of local + corpus envelopes for
    this tool name (already grouped by ``orchestrator.gather_feedback``).
    """
    feedback_text = (
        "(none)"
        if not feedback
        else "\n".join(repr(entry) for entry in feedback)
    )
    if symbol.kind == "method":
        kind_addendum = _METHOD_ADDENDUM_TEMPLATE.format(
            class_short_name=symbol.class_short_name or "?",
        )
    else:
        kind_addendum = ""

    root = symbol.root_module
    return PROMPT_TEMPLATE.format(
        qualname=symbol.qualname,
        short_name=symbol.short_name,
        kind=symbol.kind,
        module=symbol.module,
        root_module=root,
        root_alias=root,
        kind_addendum=kind_addendum,
        docstring=symbol.docstring or "(no docstring)",
        source=symbol.source,
        feedback=feedback_text,
    )
